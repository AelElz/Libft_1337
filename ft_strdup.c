#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int	i;
	char	*new;

	i = 0;
	while (s1[i])
		i++;
	new = (char *)malloc((i + 1) * sizeof(char));
	if (!new)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}